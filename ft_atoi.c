#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	i;
	int	nbr;
	int	result;

	sign = 1;
	i = 0;
	nbr = 0;
	result = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + str[i] - '0';
		i++;
	}
	result = sign * nbr;
	return (result);
}
